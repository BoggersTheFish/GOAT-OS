pub fn init() {
    unsafe {
        VGA_ENABLED = false;
    }
}

pub fn init_framebuffer(addr: *mut u8, width: u64, height: u64, pitch: u64, bpp: u16) {
    if addr.is_null() || width < (COLS * CHAR_W) as u64 || height < (ROWS * CHAR_H) as u64 || bpp < 24 {
        return;
    }
    unsafe {
        FB_ADDR = addr;
        FB_WIDTH = width as usize;
        FB_HEIGHT = height as usize;
        FB_PITCH = pitch as usize;
        FB_BPP = bpp;
        VGA_ENABLED = true;
        clear_full_screen();
        clear();
    }
}

pub fn is_enabled() -> bool {
    unsafe { VGA_ENABLED && !FB_ADDR.is_null() }
}

fn draw_char(c: u8, col: usize, row: usize) {
    if !unsafe { VGA_ENABLED } || unsafe { FB_ADDR.is_null() } {
        return;
    }
    let glyph = &FONT[(c as usize).min(127)];
    let bytes_per_pixel = (unsafe { FB_BPP } as usize + 7) / 8;
    let fg = 0x00FFFFFFu32;
    let bg = 0x00000000u32;

    unsafe {
        for dy in 0..CHAR_H {
            let fb_y = row * CHAR_H + dy;
            if fb_y >= FB_HEIGHT {
                break;
            }
            let row_bits = glyph[dy];
            for dx in 0..CHAR_W {
                let fb_x = col * CHAR_W + dx;
                if fb_x >= FB_WIDTH {
                    break;
                }
                let pixel = if (row_bits & (1 << (7 - dx))) != 0 { fg } else { bg };
                let offset = fb_y * FB_PITCH + fb_x * bytes_per_pixel;
                if bytes_per_pixel >= 4 {
                    if offset + 4 <= FB_PITCH * FB_HEIGHT {
                        let ptr = FB_ADDR.add(offset) as *mut u32;
                        *ptr = pixel;
                    }
                } else if offset + 3 <= FB_PITCH * FB_HEIGHT {
                    let ptr = FB_ADDR.add(offset);
                    *ptr.add(0) = (pixel >> 0) as u8;
                    *ptr.add(1) = (pixel >> 8) as u8;
                    *ptr.add(2) = (pixel >> 16) as u8;
                }
            }
        }
    }
}

/// Clear entire framebuffer (removes bootloader artifacts).
fn clear_full_screen() {
    if !unsafe { VGA_ENABLED } || unsafe { FB_ADDR.is_null() } {
        return;
    }
    let total_bytes = unsafe { FB_PITCH * FB_HEIGHT };
    unsafe {
        core::ptr::write_bytes(FB_ADDR, 0, total_bytes);
    }
}

pub fn clear() {
    if !unsafe { VGA_ENABLED } {
        return;
    }
    let bytes_per_pixel = (unsafe { FB_BPP } as usize + 7) / 8;
    let bg = 0u32;
    unsafe {
        for row in 0..ROWS.min(FB_HEIGHT / CHAR_H) {
            for col in 0..COLS.min(FB_WIDTH / CHAR_W) {
                for dy in 0..CHAR_H {
                    let fb_y = row * CHAR_H + dy;
                    if fb_y >= FB_HEIGHT {
                        break;
                    }
                    for dx in 0..CHAR_W {
                        let fb_x = col * CHAR_W + dx;
                        if fb_x >= FB_WIDTH {
                            break;
                        }
                        let offset = fb_y * FB_PITCH + fb_x * bytes_per_pixel;
                        if bytes_per_pixel >= 4 && offset + 4 <= FB_PITCH * FB_HEIGHT {
                            let ptr = FB_ADDR.add(offset) as *mut u32;
                            *ptr = bg;
                        } else if bytes_per_pixel == 3 && offset + 3 <= FB_PITCH * FB_HEIGHT {
                            let ptr = FB_ADDR.add(offset);
                            *ptr.add(0) = (bg >> 0) as u8;
                            *ptr.add(1) = (bg >> 8) as u8;
                            *ptr.add(2) = (bg >> 16) as u8;
                        }
                    }
                }
            }
        }
    }
    ROW.store(0, Ordering::SeqCst);
    COL.store(0, Ordering::SeqCst);
}

fn newline() {
    if !unsafe { VGA_ENABLED } {
        return;
    }
    let r = ROW.load(Ordering::SeqCst);
    if r + 1 >= CONTENT_ROWS {
        scroll();
        ROW.store(CONTENT_ROWS - 1, Ordering::SeqCst);
    } else {
        ROW.store(r + 1, Ordering::SeqCst);
    }
    COL.store(0, Ordering::SeqCst);
}

fn scroll() {
    if !unsafe { VGA_ENABLED } || unsafe { FB_ADDR.is_null() } {
        return;
    }
    let bytes_per_pixel = (unsafe { FB_BPP } as usize + 7) / 8;
    let scroll_pixel_rows = (CONTENT_ROWS - 1) * CHAR_H;
    unsafe {
        if scroll_pixel_rows * FB_PITCH <= FB_PITCH * FB_HEIGHT {
            core::ptr::copy(
                FB_ADDR.add(CHAR_H * FB_PITCH),
                FB_ADDR,
                scroll_pixel_rows * FB_PITCH,
            );
        }
        for row in (CONTENT_ROWS - 1) * CHAR_H..CONTENT_ROWS * CHAR_H {
            if row >= FB_HEIGHT {
                break;
            }
            for col in 0..COLS * CHAR_W {
                if col >= FB_WIDTH {
                    break;
                }
                let offset = row * FB_PITCH + col * bytes_per_pixel;
                if bytes_per_pixel >= 4 && offset + 4 <= FB_PITCH * FB_HEIGHT {
                    let ptr = FB_ADDR.add(offset) as *mut u32;
                    *ptr = 0;
                } else if bytes_per_pixel == 3 && offset + 3 <= FB_PITCH * FB_HEIGHT {
                    let ptr = FB_ADDR.add(offset);
                    *ptr.add(0) = 0;
                    *ptr.add(1) = 0;
                    *ptr.add(2) = 0;
                }
            }
        }
    }
    ROW.store(CONTENT_ROWS - 1, Ordering::SeqCst);
}

/// Update the bottom status bar. Call from kernel with graph stats.
pub fn update_status_bar(nodes: usize, act: u32, tension: u32) {
    if !is_enabled() {
        return;
    }
    let s = crate::alloc::format!(" nodes:{} act:{} ten:{} ", nodes, act, tension);
    let bytes = s.as_bytes();
    for (col, &b) in bytes.iter().take(COLS).enumerate() {
        draw_char(b, col, STATUS_ROW);
    }
    for col in bytes.len()..COLS {
        draw_char(b' ', col, STATUS_ROW);
    }
}

pub fn write_byte(b: u8) {
    if !unsafe { VGA_ENABLED } {
        return;
    }
    match b {
        b'\n' => newline(),
        b'\r' => COL.store(0, Ordering::SeqCst),
        _ => {
            let r = ROW.load(Ordering::SeqCst);
            let c = COL.load(Ordering::SeqCst);
            if c >= COLS {
                newline();
                write_byte(b);
                return;
            }
            draw_char(b, c, r);
            COL.store(c + 1, Ordering::SeqCst);
        }
    }
}

pub fn write_str(s: &str) {
    for b in s.bytes() {
        write_byte(b);
    }
}
